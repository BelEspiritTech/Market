package com.essot.web.controller.data;

public class RelatedProductDetails {
	
	private String relSKU;
	
	private String relProdName;
	
	private String relProdShortDesc;
	
	private Integer relProdPrice;

	public String getRelSKU() {
		return relSKU;
	}

	public void setRelSKU(String relSKU) {
		this.relSKU = relSKU;
	}

	public String getRelProdName() {
		return relProdName;
	}

	public void setRelProdName(String relProdName) {
		this.relProdName = relProdName;
	}

	public String getRelProdShortDesc() {
		return relProdShortDesc;
	}

	public void setRelProdShortDesc(String relProdShortDesc) {
		this.relProdShortDesc = relProdShortDesc;
	}

	public Integer getRelProdPrice() {
		return relProdPrice;
	}

	public void setRelProdPrice(Integer relProdPrice) {
		this.relProdPrice = relProdPrice;
	}

}
